export function useDailyMessage(): string {
  return useMemo(() => {
    const today = new Date();
    const dayOfYear =
      Math.floor(
        (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) /
          86400000
      );
    return MESSAGES[dayOfYear % MESSAGES.length];
  }, []);
}
